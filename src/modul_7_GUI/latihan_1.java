    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        String nama = "";
        String hobi = "";
        String jenisKelamin = "";
        String jurusan = "";
        String info = "";
        
        nama = txtNama.getText();
        if(cbHob1.isSelected())
            hobi += " Makan ";
        if(cbHob2.isSelected())
            hobi += " Shopping ";
        if(cbHob3.isSelected())
            hobi += " Tidur ";
        
        if(rdLaki.isSelected())
            jenisKelamin = "Laki - Laki";
        else
            jenisKelamin = "Perempuan";
        
        jurusan = lstJurusan.getSelectedValue().toString();
        
        info = "Nama saya adalah "+nama+".\n";
        info += "Hobby saya : "+hobi+"\n";
        info += "Jenis Kelamin : "+jenisKelamin+"\n";
        info += "Saya kuliah di Universitas Tokyo Jepang dan mengambil jurusan "+jurusan+".";
        JOptionPane.showMessageDialog(null, info);
    }//GEN-LAST:event_btnSubmitActionPerformed

