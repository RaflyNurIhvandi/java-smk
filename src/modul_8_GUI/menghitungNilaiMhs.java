    private void hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungActionPerformed
        //rerata
        String nama = inpNama.getText(), nim = inpNIM.getText();
        if(inpTugas.getText().isEmpty() || inpKuis.getText().isEmpty() || inpUTS.getText().isEmpty() || inpUAS.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Maaf, form masih kosong");
        } else {
            Double rata = (Double.parseDouble(inpTugas.getText()) + Double.parseDouble(inpKuis.getText()) + Double.parseDouble(inpUTS.getText()) + Double.parseDouble(inpUAS.getText()))/4;
            rerata.setText(rata.toString());
            outNama.setText(nama);
            outNIM.setText(nim);
        }
        
        //untuk grade
        Double a = Double.parseDouble(rerata.getText());
        char grd = ' ';
        if(a >= 90.00){
            grd = 'A';
            grade.setText(""+grd);
        } else if (a >= 80.00 && a <= 89.00){
            grd = 'B';
            grade.setText(""+grd);
        } else if (a >= 70.00 && a <= 79.00){
            grd = 'C';
            grade.setText(""+grd);
        } else if (a >= 40.00 && a <= 69.00){
            grd = 'D';
            grade.setText(""+grd);
        } else if (a <= 39.00){
            grd = 'E';
            grade.setText(""+grd);
        } else {
            grd = '0';
            grade.setText(""+grd);
        }
        
        //keterangan
        if(grd == 'A' || grd == 'B' || grd == 'C'){
            keterangan.setText("Dinyatakan Lulus");
        } else {
            keterangan.setText("Dinyatakan Tidak Lulus");
        }
    }//GEN-LAST:event_hitungActionPerformed

